    await callback.message.edit_text(
        "‚ùå –ó–∞—è–≤–∫–∞ –æ—Ç–º–µ–Ω–µ–Ω–∞.\n\n"
        "–ï—Å–ª–∏ –ø–µ—Ä–µ–¥—É–º–∞–µ—Ç–µ - –Ω–∞–∂–º–∏—Ç–µ /order –¥–ª—è –Ω–æ–≤–æ–π –∑–∞—è–≤–∫–∏"
    )
    await callback.answer()